//Increment and Decrement Operators

#include <iostream>
using namespace std;
int main()
{
    int a(10), b(20);
    cout<<a<<"\n";
    cout<<b<<"\n";
    cout<<a++<<" and "<<++b<<"\n";
    cout<<++a<<" and "<<b++<<"\n";
    cout<<--a<<" and "<<b--<<"\n";
    cout<<a--<<" and "<<--b<<"\n";
    cout<<a<<"\n";
    cout<<b<<"\n";
}
